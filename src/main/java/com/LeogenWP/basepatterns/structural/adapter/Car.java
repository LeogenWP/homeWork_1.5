public class Car {
    void startCar() {
        System.out.println("Starting the car...");
    }
    void shutdownCar() {
        System.out.println("Shutting down..");
    }
    void moveForward() {
        System.out.println("Moving forward..");
    }
    void moveBackward() {
        System.out.println("Moving backward..");
    }
}
